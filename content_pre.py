"""
Fear Less Maths — PRE-LEVEL Content (Pre-Primary / Class 1 entry)
Singapore-Math CPA approach: Concrete -> Pictorial -> Pictorial-Abstract bridge -> Abstract.
Every sub-level has exactly 4 sheets matching the existing Intro/Concept/Practice/Mastery
naming used everywhere else in the app, but the MEANING of each sheet is redefined here
to follow CPA stages (see table below). Remedial (R) variants are auto-generated by
remedialise() in content.py, extended to also shrink object counts in diagram params.

CPA <-> Sheet mapping used in every PreLevel sub-level:
  Sheet 1  Intro     = Concrete    : real objects, rows of <=5, plain counting
  Sheet 2  Concept   = Pictorial   : objects grouped in 5s/10s (subitizing) + concept box
  Sheet 3  Practice  = Bridge      : objects + number support (tips box, partial symbols)
  Sheet 4  Mastery   = Abstract    : numbers/symbols only, no images

Object kinds cycle through: apple, star, balloon, flower  (matches diagram_engine.py)
"""
import random
from content import cb, tb, q, remedialise  # reuse existing helpers

OBJ_KINDS = ["apple", "star", "balloon", "flower"]


def _kind(i):
    return OBJ_KINDS[i % len(OBJ_KINDS)]


def _count_q(n, kind, group_size=5):
    """One counting question: show n objects, ask the student to count & write."""
    return q("Count the objects and write the number.", "diagram",
              "Number = ____", "", "object_group",
              {"count": n, "kind": kind, "group_size": group_size})


def _compare_q(left, right, kind):
    return q("Look at both groups. Circle the group with MORE objects.", "diagram",
              "Bigger group = Left / Right", "", "object_compare",
              {"left_count": left, "right_count": right, "kind": kind})


def _draw_q(n):
    """Reverse-direction question: given a number, child draws that many objects.
    Printed worksheet leaves a blank box; no diagram needed."""
    return q(f"Draw {n} circles (●) in the box below.", "fill", "(draw in the box)")


def _one_more_q(n, kind):
    return q(f"Look at the {n} {kind}s. Draw ONE MORE and write the new total.",
             "diagram", "New total = ____", "", "object_group",
             {"count": n, "kind": kind, "group_size": 5})


def _one_less_q(n, kind):
    return q(f"Look at the {n} {kind}s. Cross out ONE and write what is left.",
             "diagram", "Now there are = ____", "", "object_group",
             {"count": n, "kind": kind, "group_size": 5})


def _gen_riddle(lo_start, hi_start, lo_change, hi_change, kind, op="more"):
    """Programmatically generate a simple addition/subtraction riddle to top up
    PreI sheets without hand-writing every single line."""
    start = random.randint(lo_start, hi_start)
    change = random.randint(lo_change, hi_change)
    if op == "more":
        ans = start + change
        verb = "come" if kind in ("balloon", "star") else "are added"
        text = f"There are {start} {kind}s. {change} more {verb}. How many now?"
    else:
        change = min(change, start - 1) if start > 1 else 1
        ans = start - change
        verb = "fly away" if kind in ("balloon", "star") else "are taken away"
        text = f"There are {start} {kind}s. {change} {verb}. How many are left?"
    return q(text, "word", f"Answer = ____ (Answer: {ans})")


# ───────────────────────── Counting blocks: PreA-PreD (1-20) ─────────────────────────

def _counting_block(lo, hi, sheet, label):
    """Builds one sheet (Intro/Concept/Practice/Mastery) for a counting range lo..hi."""
    random.seed(lo * 10 + sheet)
    span = list(range(lo, hi + 1))
    picks = [random.choice(span) for _ in range(19)]  # sample w/ replacement, enough for ~16 real Qs

    if sheet == 1:  # Concrete — plain object counting, single kind per question
        items = [cb(f"Counting {lo} to {hi}",
                     ["Touch each object and count one by one.",
                      "Say the number out loud as you touch it.",
                      "Count slowly — do not skip any object."],
                     f"Count: {lo}, {lo+1}, {lo+2}... up to {hi}")]
        for idx, n in enumerate(picks):
            items.append(_count_q(n, _kind(idx)))
        return items

    if sheet == 2:  # Pictorial — grouped in 5s, subitizing
        items = [cb("Counting in Groups of 5",
                     ["Objects are arranged in rows of 5.",
                      "Count one full row first — that is 5.",
                      "Then count the extra objects in the next row."],
                     "Row of 5 + 2 more = 7")]
        for idx, n in enumerate(picks):
            items.append(_count_q(n, _kind(idx), group_size=5))
        return items

    if sheet == 3:  # Bridge — tips box, mix of counting + reverse drawing
        items = [tb(f"Tips for Counting {lo}-{hi}",
                     ["Always start counting from 1, not from the last object.",
                      "Move your finger under each object as you count.",
                      "Check your answer by counting again."])]
        for idx, n in enumerate(picks[:12]):
            items.append(_count_q(n, _kind(idx)))
        for n in picks[12:19]:
            items.append(_draw_q(n))
        return items

    # sheet == 4: Abstract — numbers only, no images
    items = [cb(f"Numbers {lo} to {hi}",
                 ["We can also count using number words, without pictures.",
                  "Numbers go up by 1 each time.",
                  f"After {hi-1} comes {hi}."],
                 f"{hi-2}, {hi-1}, {hi}")]
    for n in picks[:10]:
        items.append(q(f"Write the number that comes just AFTER {n}.", "fill", "Answer = ____"))
    for n in picks[10:19]:
        items.append(q(f"Write the number that comes just BEFORE {n}.", "fill", "Answer = ____"))
    return items


def _A_s(sheet): return _counting_block(1, 5, sheet, "A")
def _B_s(sheet): return _counting_block(6, 10, sheet, "B")
def _C_s(sheet): return _counting_block(11, 15, sheet, "C")
def _D_s(sheet): return _counting_block(16, 20, sheet, "D")


# ───────────────────────── PreCUM1: cumulative review 1-20 ─────────────────────────

def _CUM1_s(sheet):
    random.seed(100 + sheet)
    counts = [random.choice(range(1, 21)) for _ in range(19)]
    if sheet == 1:
        items = [cb("Review: Counting 1 to 20",
                     ["This sheet mixes numbers from 1 to 20.",
                      "Count carefully — some groups are small, some are bigger."],
                     "Practice counting any group from 1 to 20")]
        items += [_count_q(n, _kind(i)) for i, n in enumerate(counts)]
        return items
    if sheet == 2:
        items = [cb("Review: Counting in Groups",
                     ["Look for full rows of 5 first.",
                      "Then add the leftover objects."],
                     "5 + 5 + 3 = 13")]
        items += [_count_q(n, _kind(i), group_size=5) for i, n in enumerate(counts)]
        return items
    if sheet == 3:
        items = [tb("Review Tips",
                     ["If you lose count, start over from 1.",
                      "Counting out loud helps you stay accurate."])]
        items += [_count_q(n, _kind(i)) for i, n in enumerate(counts[:11])]
        items += [_draw_q(n) for n in counts[11:19]]
        return items
    items = [cb("Numbers 1 to 20 — No Pictures",
                 ["You should now recognise numbers without counting objects.",
                  "Practice writing numbers in order."],
                 "1, 2, 3 ... 20")]
    for n in counts:
        items.append(q(f"What number comes right after {n}?", "fill", "Answer = ____"))
    return items


