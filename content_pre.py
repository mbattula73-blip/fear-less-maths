# ───────────────────────── PreH: Comparing groups (more/fewer) ─────────────────────────

def _H_s(sheet):
    random.seed(600 + sheet)
    pairs = [(random.randint(1, 8), random.randint(1, 8)) for _ in range(19)]
    if sheet == 1:
        items = [cb("More or Fewer",
                     ["Look at two groups of objects.",
                      "The group with MORE objects has a bigger number.",
                      "Compare by counting each group separately."],
                     "4 apples vs 6 apples -> 6 is more")]
        items += [_compare_q(l, r, _kind(i)) for i, (l, r) in enumerate(pairs)]
        return items
    if sheet == 2:
        items = [cb("Comparing Using Groups of 5",
                     ["Line up both groups in rows of 5 to compare faster.",
                      "Whichever group fills more rows is bigger."],
                     "")]
        items += [_compare_q(l, r, _kind(i)) for i, (l, r) in enumerate(pairs)]
        return items
    if sheet == 3:
        items = [tb("Comparing Tips",
                     ["Count both groups fully before deciding.",
                      "Do not guess by size of the picture — always count."])]
        for l, r in pairs:
            items.append(q(f"Which is bigger: {l} or {r}?", "fill", "Bigger = ____"))
        return items
    items = [cb("Comparing Numbers", ["Compare numbers directly without pictures."], "7 > 5")]
    for l, r in pairs:
        items.append(q(f"Fill in: {l} ___ {r}  (write > , < or =)", "fill", "Answer = ____"))
    return items


